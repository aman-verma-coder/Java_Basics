public class _9p6_dataTypes {
    public static void main(String[] args) {
        byte a = 8;
        byte b = 25;
        System.out.println(a);
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean boo = true;
        System.out.println(boo);
        double dob = 10.5;
        System.out.println(dob);
        int num = 250;
        System.out.println(num);
    }
}